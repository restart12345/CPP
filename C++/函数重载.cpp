#define  _CRT_SECURE_NO_WARNINGS 1
#include<iostream>
using namespace std;
//函数重载,提高复用性
//函数重载条件:1.函数在同一作用域下 2.函数名相同 3.函数的形参 类型不同 或者 个数不同 或者 顺序不同
void func()
{
	cout << "func()的调用" << endl;
}
void func(int a)
{
	cout << "func(int a)的调用" << endl;
}
void func(double a)
{
	cout << "func(double a)的调用" << endl;
}
void func(int a,double b)
{
	cout << "func(int a,double b)的调用" << endl;
}
void func(double a,int b)
{
	cout << "func(double a,int b)的调用" << endl;
}
int main()
{
	func();
	func(10);
	func(3.14);
	func(10,3.14);
	func(3.14,10);
	return 0;
}